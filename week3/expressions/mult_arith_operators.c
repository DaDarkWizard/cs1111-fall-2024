#include <stdio.h>

int main()
{
    int x = 4;

    printf("Multipy x by 2: %d\n", x * 2);
    printf("Divide x by 3: %d\n", x / 3);
    printf("Mod x by 3: %d\n", x % 3);
    printf("Add 1 to x: %d\n", x + 1);
    printf("Subtract 5 from x: %d\n", x - 5);

    return 0;
}
