#include <stdio.h>

int main()
{
    int x = 4;

    printf("x == 4: %d\n", x == 4);
    printf("x != 4: %d\n", x != 4);
    printf("x > 4: %d\n", x > 4);
    printf("x < 5: %d\n", x < 5);
    printf("x >= 4: %d\n", x >= 4);
    printf("x <= 3: %d\n", x <= 3);

    return 0;
}
